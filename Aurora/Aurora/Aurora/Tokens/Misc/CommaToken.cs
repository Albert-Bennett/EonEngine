namespace Aurora.Tokens.Misc
{
    /// <summary>
    /// Defines the ',' token.
    /// </summary>
    internal class CommaToken : Token
    {
        public CommaToken() { }

        public override string ToString()
        {
            return ",";
        }
    }
}
