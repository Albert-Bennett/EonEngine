namespace Eon.Rendering2D.Text
{
    /// <summary>
    /// The various ways in which a TextItem can be centered.
    /// </summary>
    public enum ScreenPositions
    {
        /// <summary>
        /// Ths screen center.
        /// </summary>
        ScreenCenter,

        /// <summary>
        /// The screen center Y.
        /// </summary>
        ScreenCenterY,

        /// <summary>
        /// The screen center X.
        /// </summary>
        ScreenCenterX,

        /// <summary>
        /// Local center.
        /// </summary>
        LocalCenter,

        /// <summary>
        /// The center Y position.
        /// </summary>
        CenterY,

        /// <summary>
        /// The center X position.
        /// </summary>
        CenterX
    }
}
